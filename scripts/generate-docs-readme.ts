function renderTable(headers: string[], rows: string[][]) {
  const lines = [
    `| ${headers.join(" | ")} |`,
    `| ${headers.map(() => "---").join(" | ")} |`,
  ]

  for (const row of rows) {
    lines.push(`| ${row.map((cell) => escapeTableCell(cell)).join(" | ")} |`)
  }

  return lines.join("\n")
}

function formatLink(fromDirectory: string, targetPath: string, label: string) {
  return `[${label}](${relativeMarkdownPath(fromDirectory, targetPath)})`
}

function relativeMarkdownPath(fromDirectory: string, targetPath: string) {
  const relativePath = toPosixPath(path.relative(fromDirectory, targetPath))

  if (relativePath.startsWith(".")) {
    return relativePath
  }

  return `./${relativePath}`
}

function stripMarkdown(value: string) {
  return value
    .replace(/`([^`]+)`/g, "$1")
    .replace(/\[([^\]]+)\]\([^)]+\)/g, "$1")
    .replace(/[*_~]/g, "")
    .replace(/!\[([^\]]*)\]\([^)]+\)/g, "$1")
    .replace(/\s+/g, " ")
    .trim()
}

function escapeTableCell(value: string) {
  return value.replace(/\|/g, "\\|")
}

function toPosixPath(value: string) {
  return value.split(path.sep).join("/")
}

function toTitleCase(value: string) {
  return value
    .split(/[\s/_-]+/)
    .filter(Boolean)
    .map((token) => {
      const normalizedToken = token.toLowerCase()

      return (
        TITLE_EXCEPTIONS[normalizedToken] ??
        `${token.charAt(0).toUpperCase()}${token.slice(1)}`
      )
    })
    .join(" ")
}

function titleFromFileName(fileName: string) {
  return toTitleCase(fileName.replace(/\.md$/i, "").replace(/[-_]/g, " "))
}

function extractScriptDescription(content: string, title: string) {
  const leadingCommentMatch = content.match(/^\/\*\*([\s\S]*?)\*\//)

  if (leadingCommentMatch) {
    const commentLines = leadingCommentMatch[1]
      .split(/\r?\n/)
      .map((line) => line.replace(/^\s*\*\s?/, "").trim())
      .filter((line) => line && !line.startsWith("@"))

    const firstNarrativeLine = commentLines.find(
      (line) => !isLowSignalDescription(stripMarkdown(line))
    )

    if (firstNarrativeLine) {
      return clampDescription(stripMarkdown(firstNarrativeLine))
    }
  }

  return `Command-line utility for ${title}, maintained in the repository \`scripts/\` directory.`
}

function extractScriptRunCommand(content: string) {
  const runCommandMatch = content.match(/run with `([^`]+)`/i)

  return runCommandMatch?.[1]?.trim()
}

function collectNarrativeBlocks(body: string) {
  const lines = body.split(/\r?\n/)
  const blocks: NarrativeBlock[] = []
  let currentBlock: string[] = []
  let inCodeBlock = false
  let currentHeading: string | undefined
  let blockIndex = 0

  const pushCurrentBlock = () => {
    if (currentBlock.length === 0) {
      return
    }

    blocks.push({
      lines: currentBlock,
      heading: currentHeading,
      blockIndex,
      isNearTop: blockIndex < 2,
    })
    currentBlock = []
    blockIndex += 1
  }

  for (const line of lines) {
    const trimmed = line.trim()

    if (trimmed.startsWith("```")) {
      pushCurrentBlock()
      inCodeBlock = !inCodeBlock
      continue
    }

    if (inCodeBlock) {
      continue
    }

    if (!trimmed) {
      pushCurrentBlock()
      continue
    }

    if (/^#{1,6}\s+/.test(trimmed)) {
      pushCurrentBlock()
      currentHeading = stripMarkdown(trimmed.replace(/^#{1,6}\s+/, "")).trim()
      continue
    }

    if (trimmed === "---") {
      pushCurrentBlock()
      continue
    }

    currentBlock.push(line)
  }

  pushCurrentBlock()

  return blocks
}

function summarizeNarrativeBlock(
  block: NarrativeBlock
): SummaryCandidate | undefined {
  const trimmedLines = block.lines.map((line) => line.trim()).filter(Boolean)

  if (trimmedLines.length === 0 || isStructuralBlock(trimmedLines)) {
    return undefined
  }

  const normalizedText = stripMarkdown(trimmedLines.join(" ")).trim()

  if (!normalizedText || isLowSignalDescription(normalizedText)) {
    return undefined
  }

  const score = scoreSummaryCandidate(block, normalizedText)

  if (score <= 0) {
    return undefined
  }

  return {
    text: clampDescription(normalizedText),
    score,
    reason: inferSummaryReason(block),
  }
}

function isStructuralBlock(lines: string[]) {
  const structuralLineCount = lines.filter((line) =>
    isStructuralLine(line)
  ).length

  return structuralLineCount === lines.length
}

function isStructuralLine(line: string) {
  return (
    line.startsWith("#") ||
    line === "---" ||
    line.startsWith("|") ||
    line.startsWith(">") ||
    line.startsWith(":::") ||
    line.startsWith("<!--") ||
    /^[-*]\s/.test(line) ||
    /^\d+\.\s/.test(line) ||
    /^\*\*[^*]+:\*\*/.test(line)
  )
}

function isLowSignalDescription(value: string) {
  return (
    value.length < 20 ||
    /^(official documentation|related documentation|documentation readiness)\b/i.test(
      value
    ) ||
    /^(topic|read|scope|guide|collection|document)\b$/i.test(value)
  )
}

function clampDescription(value: string) {
  if (value.length <= MAX_DESCRIPTION_LENGTH) {
    return value
  }

  const sentences = value
    .split(/(?<=[.!?])\s+/)
    .map((sentence) => sentence.trim())
    .filter(Boolean)

  if (sentences.length === 0) {
    return value.slice(0, MAX_DESCRIPTION_LENGTH).trimEnd()
  }

  let description = ""

  for (const sentence of sentences) {
    const candidate = description ? `${description} ${sentence}` : sentence

    if (candidate.length > MAX_DESCRIPTION_LENGTH) {
      break
    }

    description = candidate

    if (description.length >= MIN_PREFERRED_DESCRIPTION_LENGTH) {
      break
    }
  }

  return description || value.slice(0, MAX_DESCRIPTION_LENGTH).trimEnd()
}

function buildFallbackDescription(title: string) {
  if (/auth/i.test(title)) {
    return `Project guidance for ${title}, including integration choices, security constraints, and implementation expectations.`
  }

  if (/api/i.test(title)) {
    return `Project guidance for ${title}, including contracts, route behavior, and implementation expectations.`
  }

  if (/database|drizzle|postgres/i.test(title)) {
    return `Project guidance for ${title}, including schema conventions, persistence concerns, and implementation expectations.`
  }

  if (/test|vitest/i.test(title)) {
    return `Project guidance for ${title}, including quality expectations, validation flow, and implementation conventions.`
  }

  return `Project guidance for ${title}, including conventions, references, and implementation expectations.`
}

function scoreSummaryCandidate(block: NarrativeBlock, text: string) {
  const heading = block.heading?.toLowerCase() ?? ""
  let score = 0

  if (!block.heading && block.blockIndex === 0) {
    score += 120
  } else if (block.isNearTop) {
    score += 30
  }

  if (heading.includes("how we use") || heading.includes("how we wire")) {
    score += 100
  } else if (heading.includes("map to")) {
    score += 80
  } else if (heading.includes("red flags")) {
    score += 35
  } else if (heading.includes("related documentation")) {
    score -= 100
  } else if (heading.includes("official documentation")) {
    score -= 80
  } else if (heading) {
    score += 20
  }

  if (containsUsefulDomainSignals(text)) {
    score += 25
  }

  if (text.length >= 60 && text.length <= 155) {
    score += 25
  } else if (text.length > MAX_DESCRIPTION_LENGTH) {
    score -= 10
  }

  if (/\b(not|never|avoid|only)\b/i.test(text)) {
    score += 10
  }

  if (/^\w+\s+guide\b/i.test(text)) {
    score -= 20
  }

  return score
}

function inferSummaryReason(block: NarrativeBlock): SummaryCandidate["reason"] {
  const heading = block.heading?.toLowerCase() ?? ""

  if (!block.heading && block.blockIndex === 0) {
    return "opening-paragraph"
  }

  if (heading.includes("how we use") || heading.includes("how we wire")) {
    return "how-we-use"
  }

  if (heading.includes("map to")) {
    return "map-to"
  }

  if (heading.includes("red flags")) {
    return "red-flags"
  }

  if (heading) {
    return "section-intro"
  }

  return "generic-block"
}

function containsUsefulDomainSignals(text: string) {
  return /\b(guidance|integration|conventions|implementation|auth|routing|validation|migration|server|client|state|deployment|testing|schema|permissions|security|api)\b/i.test(
    text
  )
}

async function writeIfChanged(filePath: string, nextContent: string) {
  const normalizedNextContent = await formatGeneratedContent(
    filePath,
    nextContent
  )
  const currentContent = await fs.readFile(filePath, "utf8").catch(() => "")

  if (currentContent === normalizedNextContent) {
    return
  }

  await fs.writeFile(filePath, normalizedNextContent, "utf8")
}

async function formatGeneratedContent(filePath: string, content: string) {
  if (path.extname(filePath).toLowerCase() !== ".md") {
    return content
  }

  const prettierConfig = await prettier.resolveConfig(filePath)

  return prettier.format(content, {
    ...prettierConfig,
    filepath: filePath,
  })
}

function withTrailingNewline(content: string) {
  return content.endsWith("\n") ? content : `${content}\n`
}

