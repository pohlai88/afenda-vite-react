async function main() {
  const afendaConfig = await loadAfendaConfig()
  const docsRoot = path.join(workspaceRoot, "docs")

  assertDependencyCategoryConfiguration()

  await writeIfChanged(
    path.join(docsRoot, "OPERATING_MAP.md"),
    await renderOperatingMap(docsRoot, workspaceRoot, afendaConfig)
  )

  const targets = await resolveReadmeTargets(
    workspaceRoot,
    afendaConfig.readmeTargets
  )

  for (const target of targets) {
    if (target.mode === "docs-root") {
      await writeIfChanged(
        path.join(target.absolutePath, "README.md"),
        await renderRootReadme(target.absolutePath, afendaConfig)
      )
      continue
    }

    if (target.mode === "docs-collections") {
      const docsRoot = path.join(workspaceRoot, "docs")
      const relativeToDocs = toPosixPath(
        path.relative(docsRoot, target.absolutePath)
      )

      if (relativeToDocs === "dependencies") {
        await writeIfChanged(
          path.join(target.absolutePath, "README.md"),
          await renderDependenciesReadme(target.absolutePath, docsRoot)
        )
        continue
      }

      await writeIfChanged(
        path.join(target.absolutePath, "README.md"),
        await renderGenericReadme(target.absolutePath, docsRoot)
      )
      continue
    }

    if (target.mode === "generic-formal-directory") {
      await writeIfChanged(
        path.join(target.absolutePath, "README.md"),
        await renderFormalDirectoryReadme(target, workspaceRoot)
      )
      continue
    }

    await writeIfChanged(
      path.join(target.absolutePath, "README.md"),
      await renderScriptsReadme(
        target.absolutePath,
        workspaceRoot,
        afendaConfig
      )
    )
  }
}

async function resolveReadmeTargets(
  workspaceRoot: string,
  readmeTargets: ReadmeTargetDefinition[]
): Promise<ResolvedReadmeTarget[]> {
  const resolvedTargets: ResolvedReadmeTarget[] = []

  for (const target of readmeTargets) {
    const absolutePath = path.join(workspaceRoot, target.path)

    if (target.mode === "docs-collections") {
      const collectionDirectories = await discoverDirectoriesWithDocs(
        absolutePath,
        false
      )

      resolvedTargets.push(
        ...collectionDirectories.map((directory) => ({
          absolutePath: directory,
          relativePath: toPosixPath(path.relative(workspaceRoot, directory)),
          mode: target.mode,
          title: target.title,
          description: target.description,
        }))
      )
      continue
    }

    resolvedTargets.push({
      absolutePath,
      relativePath: toPosixPath(path.relative(workspaceRoot, absolutePath)),
      mode: target.mode,
      title: target.title,
      description: target.description,
      includeChildDirectories: target.includeChildDirectories,
    })
  }

  return resolvedTargets.sort((left, right) =>
    left.relativePath.localeCompare(right.relativePath)
  )
}

async function discoverDirectoriesWithDocs(
  rootDirectory: string,
  includeRoot = true
) {
  const directories = new Set<string>(includeRoot ? [rootDirectory] : [])

  async function walk(directory: string) {
    const children = await fs.readdir(directory, { withFileTypes: true })

    for (const child of children) {
      if (!child.isDirectory()) {
        continue
      }

      const childDirectory = path.join(directory, child.name)
      const childFiles = await fs.readdir(childDirectory, {
        withFileTypes: true,
      })
      const hasMarkdownDocs = childFiles.some(
        (entry) =>
          entry.isFile() &&
          entry.name.endsWith(".md") &&
          entry.name !== "README.md"
      )
      const hasReadme = childFiles.some(
        (entry) => entry.isFile() && entry.name === "README.md"
      )

      if (hasMarkdownDocs || hasReadme) {
        directories.add(childDirectory)
      }

      await walk(childDirectory)
    }
  }

  await walk(rootDirectory)

  return Array.from(directories).sort((left, right) =>
    left.localeCompare(right)
  )
}

